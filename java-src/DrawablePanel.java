    @Override
    public void paintComponent(java.awt.Graphics g){
        super.paintComponent(g);
        if(image != null){
            g.drawImage(image, 0, 0, this);
        }
        if(scDetection != null) {
            java.util.ArrayList<detect.BoundingBox> bbList = scDetection.getBbList();
            for(int i=0; i<bbList.size();i++){
                detect.BoundingBox bb = bbList.get(i);
                g.setColor(Color.RED);
                g.drawRect(bb.getX1(), bb.getY1(), bb.getX2()-bb.getX1(), bb.getY2()-bb.getY1());
                
            }
        }
    }

